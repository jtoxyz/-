CREATE OR REPLACE FUNCTION public.audit_blacklist_change()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_action text;
  v_id uuid;
  v_details jsonb;
BEGIN
  IF TG_OP = 'INSERT' THEN
    v_action := 'blacklist_add';
    v_id := NEW.id;
    v_details := to_jsonb(NEW);
  ELSIF TG_OP = 'UPDATE' THEN
    v_action := CASE
      WHEN OLD.active IS TRUE AND NEW.active IS FALSE THEN 'blacklist_remove'
      WHEN OLD.active IS FALSE AND NEW.active IS TRUE THEN 'blacklist_restore'
      ELSE 'blacklist_update'
    END;
    v_id := NEW.id;
    v_details := jsonb_build_object('before', to_jsonb(OLD), 'after', to_jsonb(NEW));
  ELSE
    v_action := 'blacklist_delete';
    v_id := OLD.id;
    v_details := to_jsonb(OLD);
  END IF;

  INSERT INTO public.admin_action_logs(
    action, target_table, target_id, details, actor_user_id
  ) VALUES (
    v_action, 'user_blacklist', v_id::text, v_details, auth.uid()
  );

  IF TG_OP = 'DELETE' THEN
    RETURN OLD;
  END IF;
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS user_blacklist_audit ON public.user_blacklist;
CREATE TRIGGER user_blacklist_audit
AFTER INSERT OR UPDATE OR DELETE ON public.user_blacklist
FOR EACH ROW EXECUTE FUNCTION public.audit_blacklist_change();

