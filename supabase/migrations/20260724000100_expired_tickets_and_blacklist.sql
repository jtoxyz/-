CREATE OR REPLACE FUNCTION public.normalize_blacklist_entry()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_student text;
BEGIN
  IF NEW.student_number IS NOT NULL THEN
    v_student := upper(regexp_replace(btrim(NEW.student_number), '\s+', '', 'g'));
    IF v_student LIKE 'S%' THEN
      v_student := substr(v_student, 2);
    END IF;
    NEW.student_number := NULLIF(v_student, '');
  END IF;

  IF NEW.university_email IS NOT NULL THEN
    NEW.university_email := NULLIF(lower(btrim(NEW.university_email)), '');
  END IF;

  NEW.reason := btrim(NEW.reason);
  NEW.updated_at := now();

  IF TG_OP = 'INSERT' THEN
    NEW.created_by := COALESCE(NEW.created_by, auth.uid());
  END IF;

  IF NEW.active IS FALSE THEN
    IF TG_OP = 'INSERT' OR OLD.active IS DISTINCT FROM FALSE THEN
      NEW.removed_at := COALESCE(NEW.removed_at, now());
      NEW.removed_by := COALESCE(NEW.removed_by, auth.uid());
    END IF;
  ELSIF NEW.active IS TRUE THEN
    NEW.removed_at := NULL;
    NEW.removed_by := NULL;
  END IF;

  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS user_blacklist_normalize ON public.user_blacklist;
CREATE TRIGGER user_blacklist_normalize
BEFORE INSERT OR UPDATE ON public.user_blacklist
FOR EACH ROW EXECUTE FUNCTION public.normalize_blacklist_entry();

CREATE OR REPLACE FUNCTION public.is_user_blacklisted(
  p_student_number text,
  p_university_email text
)
RETURNS boolean
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_student text;
  v_email text;
BEGIN
  v_student := upper(regexp_replace(btrim(COALESCE(p_student_number, '')), '\s+', '', 'g'));
  IF v_student LIKE 'S%' THEN
    v_student := substr(v_student, 2);
  END IF;
  v_email := lower(btrim(COALESCE(p_university_email, '')));

  RETURN EXISTS (
    SELECT 1
    FROM public.user_blacklist b
    WHERE b.active
      AND (b.expires_at IS NULL OR b.expires_at > now())
      AND (
        (v_student <> '' AND b.student_number = v_student)
        OR (v_email <> '' AND b.university_email = v_email)
      )
  );
END;
$$;

REVOKE ALL ON FUNCTION public.is_user_blacklisted(text, text) FROM PUBLIC;

