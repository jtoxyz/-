CREATE FUNCTION public.find_ticket(
  p_event_id uuid,
  p_event_slot_id uuid,
  p_student_name text,
  p_student_number text,
  p_university_email text
)
RETURNS text
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_public_token text;
  v_student_number text;
  v_email text;
BEGIN
  v_student_number := upper(regexp_replace(btrim(COALESCE(p_student_number, '')), '\s+', '', 'g'));
  IF v_student_number LIKE 'S%' THEN
    v_student_number := substr(v_student_number, 2);
  END IF;
  v_email := lower(btrim(COALESCE(p_university_email, '')));

  IF public.is_user_blacklisted(v_student_number, v_email) THEN
    RETURN NULL;
  END IF;

  SELECT r.public_token
    INTO v_public_token
  FROM public.reservations r
  JOIN public.events e ON e.id = r.event_id
  LEFT JOIN public.event_slots es ON es.id = r.event_slot_id
  WHERE r.event_id = p_event_id
    AND r.event_slot_id = p_event_slot_id
    AND r.student_name = btrim(p_student_name)
    AND r.student_number = v_student_number
    AND r.university_email = v_email
    AND r.status IN ('reserved', 'used')
    AND (
      CASE
        WHEN r.ticket_type = 'walkin' THEN
          COALESCE(es.walkin_use_ends_at, es.walkin_ends_at, es.ticket_use_ends_at, e.use_ends_at, es.ends_at)
        ELSE
          COALESCE(es.reservation_use_ends_at, es.ticket_use_ends_at, e.use_ends_at, es.ends_at)
      END IS NULL
      OR now() <= CASE
        WHEN r.ticket_type = 'walkin' THEN
          COALESCE(es.walkin_use_ends_at, es.walkin_ends_at, es.ticket_use_ends_at, e.use_ends_at, es.ends_at)
        ELSE
          COALESCE(es.reservation_use_ends_at, es.ticket_use_ends_at, e.use_ends_at, es.ends_at)
      END
    )
  ORDER BY r.created_at DESC
  LIMIT 1;

  RETURN v_public_token;
END;
$$;

